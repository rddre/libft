#include "libft.h"

void	ft_bzero(void	*str, size_t	nb)
{
	size_t	i;
	char	*strr;

	strr = (char *) str;
	i = 0;
	while (i < nb)
	{
		strr[i] = '\0';
		i++;
	}
}
